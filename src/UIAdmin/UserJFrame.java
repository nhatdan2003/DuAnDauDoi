    // set table
    public void initModel() {
        setIconImage(ShareHelper.APP_ICON);
        model = new DefaultTableModel(new Object[0][], (Object[]) new String[]{"Fullname", "Username", "Password", "Gender", "Birthday", "Phone", "Email", "Address", "Role"}) {
            boolean[] canEdit = new boolean[]{false, false, false, false, false, false, false, false, false, false};

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return this.canEdit[columnIndex];
            }
        };
        tblUser.setModel(model);
        setLocationRelativeTo(null);
        fillTable();
        row = -1;
        updateStatus();
        selectradio();
        this.setExtendedState(JFrame.MAXIMIZED_BOTH);
        txttimKiem.setForeground(Color.GRAY);
        txttimKiem.setText("Search by Fullname or Username");
    }// set tblUser

    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblUser.getModel();
        model.setRowCount(0);
        try {
            String keyword = txttimKiem.getText();
            List<Account> list = dao.selectByKeyword(keyword);
            for (Account ac : list) {
                Object[] row = {ac.getFullName(), ac.getUserName(), ac.getPassWord(), ac.isGender() ? "Nữ" : "Nam",
                    ac.getBirthDay(), ac.getPhone(), ac.getEmail(), ac.getAddRess(), ac.isRole() ? "Quản Lý" : "Nhân Viên"
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            System.out.println(e);
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    void last() {
        this.row = tblUser.getRowCount() - 1;
        this.edit();
    }

    void next() {
        if (this.row < tblUser.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
    }

    void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }

    void first() {
        this.row = 0;
        this.edit();
    }

    void edit() {
        String manv = (String) tblUser.getValueAt(this.row, 1);
        Account ac = dao.selectById(manv);
        this.setForm(ac);
        tabs.setSelectedIndex(0);
        this.updateStatus();
    }

    void setForm(Account ac) {
        ArrayList<Account> list = new ArrayList<>();
        System.out.println(list);
        txtHoTen.setText(ac.getFullName());
        txtusername.setText(ac.getUserName());
        txtPassword.setText(ac.getPassWord());
        txtSDT.setText(String.valueOf(ac.getPhone()));
        txtDiaChi.setText(ac.getAddRess());
        txtEmail.setText(ac.getEmail());
        rdonhanvien.setSelected(!ac.isRole());
        rdoquanly.setSelected(ac.isRole());
        rdoNam.setSelected(!ac.isGender());
        rdoNu.setSelected(ac.isGender());
    }

    Account getForm() {
        Account ac = new Account();
        ac.setFullName(txtHoTen.getText());
        ac.setUserName(txtusername.getText());
        ac.setPassWord(txtPassword.getText());
        ac.setAddRess(txtDiaChi.getText());
        ac.setPhone(txtSDT.getText());
        ac.setEmail(txtEmail.getText());
        ac.setRole(rdoquanly.isSelected());
        ac.setGender(rdoNu.isSelected());
        return ac;
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblUser.getRowCount() - 1);

        txtusername.setEditable(!edit);
        btnthem.setEnabled(!edit);
        btnsua.setEnabled(edit);
        btnxoa.setEnabled(edit);

        btnfirst.setEnabled(edit && !first);
        btnprev.setEnabled(edit && !first);
        btnnext.setEnabled(edit && !last);
        btnlast.setEnabled(edit && !last);
    }

    void clearForm() {
        txtHoTen.setText("");
        txtusername.setText("");
        txtPassword.setText("");
        txtDiaChi.setText("");
        txtSDT.setText("");
        txtEmail.setText("");
        selectradio();
        this.row = -1;
        this.updateStatus();
    }

    void selectradio() {
        rdoNam.setSelected(true);
        rdonhanvien.setSelected(true);
    }

    void insert() {
        if (validateInsert() == true) {
            Account ac = getForm();
            try {
                dao.insert(ac);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "INSERT is success!");
            } catch (Exception e) {
                MsgBox.alert(this, "INSERT is fail!");
            }
        }
    }

    void delete() {
        String manv = txtusername.getText();
        if (manv.equals(Auth.userName.getUserName())) {
            MsgBox.alert(this, "You can't DELETE yourself!");
        } else if (MsgBox.confirm(this, "Are you sure for DELETE?")) {
            try {
                dao.delete(manv);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "DELETE is success!");
            } catch (Exception e) {
                MsgBox.alert(this, "DELETE is fail!");
            }
        }
    }

    void update() {
        if (ValidateUpdate() == true) {
            Account ac = getForm();
            try {
                dao.update(ac);
                this.fillTable();
                MsgBox.alert(this, "UPDATE is success!");
            } catch (Exception e) {
                MsgBox.alert(this, "UPDATE is fail!");
            }
        }
    }

    boolean validateInsert() {
        if (txtHoTen.getText().isEmpty()) {
            MsgBox.alert(this, "Fullname can not be empty!");
            return false;
        }
        if (txtusername.getText().isEmpty()) {
            MsgBox.alert(this, "Username can not be empty!");
            return false;
        }
        if (txtPassword.getText().isEmpty()) {
            MsgBox.alert(this, "Password can not be empty!");
            return false;
        }
        if (txtSDT.getText().isEmpty()) {
            MsgBox.alert(this, "Phone number can not be empty!");
            return false;
        }
        if (!txtSDT.getText().matches("[0][0-9]{9}")) {
            MsgBox.alert(this, "WRONG Phone number format!");
            return false;
        }
        if (txtEmail.getText().isEmpty()) {
            MsgBox.alert(this, "Email can not be empty!");
            return false;
        }
        if (!txtEmail.getText().matches("^\\w+@\\w+(.\\w+){1,2}")) {
            MsgBox.alert(this, "WRONG Email format");
            return false;
        }
        if (txtDiaChi.getText().isEmpty()) {
            MsgBox.alert(this, "Address can not be empty!");
            return false;
        }
        return true;
    }

    boolean ValidateUpdate() {
        if (txtHoTen.getText().isEmpty()) {
            MsgBox.alert(this, "Fullname can not be empty!");
            return false;
        }
        if (txtPassword.getText().isEmpty()) {
            MsgBox.alert(this, "Password can not be empty!");
            return false;
        }
        if (txtSDT.getText().isEmpty()) {
            MsgBox.alert(this, "Phone number can not be empty!");
        }

        if (!txtSDT.getText().matches("[0][0-9]{9}")) {
            MsgBox.alert(this, "WRONG Phone number format!");
            return false;
        }
        if (txtEmail.getText().isEmpty()) {
            MsgBox.alert(this, "Email can not be empty!");
            return false;
        }
        if (!txtEmail.getText().matches("^\\w+@\\w+(.\\w+){1,2}")) {
            MsgBox.alert(this, "WRONG Email format!");
            return false;
        }
        if (txtDiaChi.getText().isEmpty()) {
            MsgBox.alert(this, "Address can not be empty!");
            return false;
        }

        return true;
    }

